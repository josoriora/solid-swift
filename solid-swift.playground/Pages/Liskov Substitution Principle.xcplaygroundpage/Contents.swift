 # Liskov Substitution Principle

 Also known as design by contract. Parent classes should be able to be replaced by chlid classes
 without modifying the behavior of the system.
 Expressed in a formal way:
 
 Given S is a subtype of T, then objects of type T can be replaced with objects of type S without altering
 any of the properties of the system


